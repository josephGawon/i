            let pebble=grain.replaceAll("O", "&oacute;");
            let stone=pebble.replaceAll("U", "&uacute;");
            let boulder=stone.replaceAll("QQ", "&#x24E;");
            let mountain=boulder.replaceAll("qq","&#x24F;");
            let island=mountain.replaceAll("QY", "&#x24E;");
