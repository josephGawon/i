        function ipaLet(x){//phonetics function
          let ayy=x.replace("#","")

          let ay=ayy.replaceAll("btrill","&#665;");//note: not th.
          let axx=ay.replaceAll("y","j");//wai
          let ax=axx.replaceAll("ng","&#331;");//ng symbol
          let a=ax.replaceAll("a","a");//open central unrounded vowel /AKA awed ata la'n a
          let b=a.replaceAll("e","&#603;");//open midFront unrounded vowel/ aka vfilekted un 3 
          let c=b.replaceAll("i","&#618;");//nearClose nearFront unrounded vowel/ short i
          let d=c.replaceAll("I","i");//tagalog i
          let e=d.replaceAll("O","&#596;");//midBack rounded vowel
          let f=e.replaceAll("o","&#111;&#798;");//m
          let g=f.replaceAll("u","&#117;");
          let h=g.replaceAll("U","y");
          let i=h.replaceAll("sh","&#643;");
          let j=i.replaceAll("Z","d&#865;&#658;");
          let k=j.replaceAll("g","g&#865;&#611;");//lub g
          let l=k.replaceAll("G","g");//non lub g
          let m=l.replaceAll("k","k&#865;x'");
          let n=m.replaceAll("K","k");//non lub k
          let o=n.replaceAll("vf","&#112;&#810;&#865;f");//vf inp
          let p=o.replaceAll("@","&#712;");//stress is @ //MAYBE 
          let q=p.replaceAll("--","&#720;");//elongation --> double hyphen inp
          let r=q.replaceAll("?","&#660;");//glottal stop --> <?> inp
          let s=r.replaceAll("r","&#638;")
          let t=s.replaceAll("ch","t&#865;&#643;");
          let u=t.replaceAll("qq","&#613;")
          let v=u.replaceAll("E","e")
          let w=v.replaceAll("'","&#712;")//SAME AS @, apostrophe turns into raised vertical line to indicate stress
          
          return w;
        }

    
